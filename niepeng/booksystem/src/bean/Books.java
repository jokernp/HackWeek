package bean;

public class Books {
	private int id;
	private String type;
	private String name;
	private String author;
	private String press;
	private int price;
	private String time;
	private int number;
	private String remark;
	private int state;
	private String js;
	public void setId(int i){
		id=i;
	}
	public int getId(){
		return id;
	}
	public void setType(String ty){
		type=ty;
	}
	public String getType(){
		return type;
	}
	public void setName(String na){
		name=na;
	}
	public String getName(){
		return name;
	}
	public void setAuthor(String au){
		author=au;
	}
	public String getAuthor(){
		return author;
	}
	public void setPress(String pr){
		press=pr;
	}
	public String getPress(){
		return press;
	}
	public void setPrice(int pr){
		price=pr;
	}
	public int getPrice(){
		return price;
	}
	public void setTime(String ti){
		time=ti;
	}
	public String getTime(){
		return time;
	}
	public void setNumber(int nu){
		number=nu;
	}
	public int getNumber(){
		return number;
	}
	public void setRemark(String re){
		remark=re;
	}
	public String getRemark(){
		return remark;
	}
	public void setState(int st){
		state=st;
	}
	public int getState(){
		return state;
	}
	public void setJs(String j){
		js=j;
	}
	public String getJs(){
		return js;
	}

}