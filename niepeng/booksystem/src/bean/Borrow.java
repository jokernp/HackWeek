package bean;

public class Borrow {
	private String username;
	private String time;
	private int id;
	public void setUsername(String ue){
		username=ue;
	}
	public String getUsername(){
		return username;
	}
	public void setId(int i){
		id=i;
	}
	public int getId(){
		return id;
	}
	public void setTime(String ti){
		time=ti;
	}
	public String getTime(){
		return time;
	}
}
